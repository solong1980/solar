package com.solar.command.message.response.app;

import java.io.IOException;

import com.solar.command.message.response.ServerResponse;
import com.solar.common.context.ConnectAPI;

public class VCodeResponse extends ServerResponse {

	public VCodeResponse(String msg) {
		super(0, ConnectAPI.VCODE_GET_RESPONSE);
		try {
			output.writeUTF(msg);
		} catch (IOException e) {
			logger.error("output write fail", e);
		} finally {
			output.close();
		}
	}

}
